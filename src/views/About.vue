    <h1 class="title">Qu'est ce que TraLELHo</h1>

    Dans les hôpitaux, l’accueil de personnes étrangères n’est pas toujours
    simple. Comment demander en effet à un patient ne parlant pas le français
    d’expliquer ses douleurs ou ses antécédents médicaux ? Un casse-tête
    linguistique auquel tous les personnels médicaux ont un jour été confrontés.
    Tralelho est un site qui permet de traduire les phrases utilisées par les
    services de secours ou de soins dans un maximum de langues. A ce jour 101
    langues sont traduites ou en cours de traductions.

